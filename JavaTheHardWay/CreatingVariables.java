public class CreatingVariables {
  public static void main( String[] args ) {
    int x, y, age, ageage;
    double seconds, e, checking, piNum;
    String firstName, lastName, title, realName, howTall;


    x = 10;
    y = 400;
    age = 39;
    ageage = 29;

    seconds = 4.71;
    e = 2.71828182845904523536;
    checking = 1.89;
    piNum = 3.1415;

    firstName = "Graham";
    lastName = "Mitchell";
    title = "Mr.";
    realName = "Andrew Lyon";
    howTall = "6'3\"";

    System.out.println( "The variable x contains " + x );
    System.out.println( "The value " + y + " is stored in the variable y." );
    System.out.println( "The experiment took " + seconds + " seconds." );
    System.out.println( "A favorite irrational # is Euler's number: " + e );
    System.out.println( "Hopefully you have more than $" + checking + "!" );
    System.out.println( "My name's " + title + " " + firstName + lastName );
    System.out.println( "Just kidding, my real name is " + realName + "!");
    System.out.println( "I'm " + howTall + " tall!");
    System.out.println( "And my favorite number is " + piNum + ".");
    System.out.println( "And I'm really " + ageage+ " years old!");
  }
}
